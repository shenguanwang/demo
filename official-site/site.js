function initializeSiteMotion() {
  const reduceMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
  document.body.classList.add("motion-ready");

  const groups = [
    [".company-hero .hero-copy, .company-hero .proof-panel, .page-hero > *, .luxeed-hero-copy", "reveal-from-left"],
    [".section-heading, .dealer-copy, .dealer-requirements, .aito-export-note > *, .r7-source-row > *, .edition-feature-copy > *", ""],
    [".role-grid article, .choice-card, .export-service-list article, .market-board article, .process-map li, .spec-summary-grid > div, .spec-note-grid > article, .r7-overview > div, .edition-facts > div", "reveal-scale"],
    [".aito-model, .spec-table-wrap, .inquiry-form, .faq-list, .luxeed-hero", "reveal-scale"]
  ];

  const revealItems = [];
  groups.forEach(([selector, variant]) => {
    document.querySelectorAll(selector).forEach((element, index) => {
      element.classList.add("reveal-item");
      if (variant) element.classList.add(variant);
      element.style.setProperty("--reveal-delay", `${Math.min(index % 4, 3) * 70}ms`);
      revealItems.push(element);
    });
  });

  if (reduceMotion || !("IntersectionObserver" in window)) {
    revealItems.forEach((element) => element.classList.add("is-visible"));
  } else {
    const revealObserver = new IntersectionObserver((entries, observer) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) return;
        entry.target.classList.add("is-visible");
        observer.unobserve(entry.target);
      });
    }, { rootMargin: "0px 0px -9%", threshold: 0.08 });
    revealItems.forEach((element) => revealObserver.observe(element));
  }

  const header = document.querySelector(".site-header");
  const firstSection = document.querySelector("main > section:first-child");
  if (header && firstSection && "IntersectionObserver" in window) {
    const headerObserver = new IntersectionObserver(([entry]) => {
      header.classList.toggle("is-scrolled", !entry.isIntersecting);
    }, { rootMargin: "-72px 0px 0px", threshold: 0 });
    headerObserver.observe(firstSection);
  }

  const aitoModels = [...document.querySelectorAll(".aito-model")];
  const aitoLinks = [...document.querySelectorAll(".aito-model-nav a[href^='#']")];
  if (aitoModels.length && aitoLinks.length && "IntersectionObserver" in window) {
    const modelObserver = new IntersectionObserver((entries) => {
      const current = entries
        .filter((entry) => entry.isIntersecting)
        .sort((a, b) => b.intersectionRatio - a.intersectionRatio)[0];
      if (!current) return;
      aitoLinks.forEach((link) => {
        const active = link.getAttribute("href") === `#${current.target.id}`;
        link.classList.toggle("is-active", active);
        if (active) link.setAttribute("aria-current", "true");
        else link.removeAttribute("aria-current");
      });
    }, { rootMargin: "-18% 0px -58%", threshold: [0.08, 0.2, 0.45] });
    aitoModels.forEach((model) => modelObserver.observe(model));
  }

  document.querySelectorAll(".proof-panel strong").forEach((node) => {
    const match = node.textContent.trim().match(/^([\d,]+)(.*)$/);
    if (!match) return;
    const target = Number(match[1].replace(/,/g, ""));
    const suffix = match[2];
    node.dataset.countTo = String(target);
    if (reduceMotion || target > 100000) return;
    const countObserver = new IntersectionObserver(([entry], observer) => {
      if (!entry.isIntersecting) return;
      observer.unobserve(node);
      const started = performance.now();
      const duration = 950;
      const tick = (now) => {
        const progress = Math.min((now - started) / duration, 1);
        const eased = 1 - Math.pow(1 - progress, 3);
        node.textContent = `${Math.round(target * eased).toLocaleString()}${suffix}`;
        if (progress < 1) requestAnimationFrame(tick);
      };
      requestAnimationFrame(tick);
    }, { threshold: 0.7 });
    countObserver.observe(node);
  });
}

